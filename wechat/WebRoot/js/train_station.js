		!$("#toZh").val() && $("#toZh").val('到达城市');
		
		if($.trim($("#fromZh").val())!="" && $.trim($("#fromZh").val())!="出发城市"){
			hideErrMsg("fromZh");
		}
		if($.trim($("#toZh").val())!="" && $.trim($("#toZh").val())!="到达城市"){
			hideErrMsg("toZh");
		}
