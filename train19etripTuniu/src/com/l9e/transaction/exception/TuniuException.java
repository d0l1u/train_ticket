package com.l9e.transaction.exception;

/**
 * 途牛顶层异常
 * @author licheng
 *
 */
public class TuniuException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2301139928838639246L;

	public TuniuException() {
		super();
	}

	public TuniuException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TuniuException(String arg0) {
		super(arg0);
	}

	public TuniuException(Throwable arg0) {
		super(arg0);
	}

}
