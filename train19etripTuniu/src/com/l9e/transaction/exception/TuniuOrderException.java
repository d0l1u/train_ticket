package com.l9e.transaction.exception;

/**
 * 途牛订单业务异常
 * @author licheng
 *
 */
public class TuniuOrderException extends TuniuException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8263090775310052322L;

	public TuniuOrderException() {
		super();
	}

	public TuniuOrderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TuniuOrderException(String arg0) {
		super(arg0);
	}

	public TuniuOrderException(Throwable arg0) {
		super(arg0);
	}

}
