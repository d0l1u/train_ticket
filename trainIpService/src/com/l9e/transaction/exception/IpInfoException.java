package com.l9e.transaction.exception;

/**
 * IP业务异常
 * @author wangsf
 *
 */
public class IpInfoException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6558379420333896425L;

	public IpInfoException() {
		super();
	}

	public IpInfoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IpInfoException(String arg0) {
		super(arg0);
	}

	public IpInfoException(Throwable arg0) {
		super(arg0);
	}

}
