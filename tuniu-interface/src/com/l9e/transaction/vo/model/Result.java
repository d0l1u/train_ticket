package com.l9e.transaction.vo.model;

import java.util.Map;

/**
 * 出参规范
 * @author licheng
 *
 */
public interface Result {

	/**
	 * 设置返回码
	 * @param code
	 */
	void setCode(String code);
	/**
	 * 返回码
	 * @return
	 */
	String getCode();
	/**
	 * 处理结果映射集
	 * @return
	 */
	Map<String, Object> resultMap();
	/**
	 * 数据存入结果集
	 * @param key
	 * @param value
	 */
	void putData(String key, Object value);
}
