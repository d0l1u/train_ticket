package com.l9e.transaction.vo;

/**
 * @author Administrator
 * 插入出票系统日志：cp_orderinfo_history
 */

public class CpSysLogVO {
	
	private String order_id;
	private String order_optlog;
	private String create_time;
	private String opter;
	
	public CpSysLogVO(String order_id, String order_optlog, String create_time,
			String opter) {
		super();
		this.order_id = order_id;
		this.order_optlog = order_optlog;
		this.create_time = create_time;
		this.opter = opter;
	}
	
	public CpSysLogVO() {
		
	}

	public String getOrder_id() {
		return order_id;
	}
	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}
	public String getOrder_optlog() {
		return order_optlog;
	}
	public void setOrder_optlog(String order_optlog) {
		this.order_optlog = order_optlog;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getOpter() {
		return opter;
	}
	public void setOpter(String opter) {
		this.opter = opter;
	}
	
}
