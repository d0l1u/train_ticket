package com.l9e.transaction.vo;

/**
 * 途牛退款车票
 * 
 * @author licheng
 * 
 */
public class TuniuRefundTicket {

	/**
	 * 车票号
	 */
	private String ticketNo;
	/**
	 * 乘客姓名
	 */
	private String passengerName;
	/**
	 * 乘客证件类型
	 */
	private String passportTypeId;
	/**
	 * 乘客证件号码
	 */
	private String passportNo;

	/**
	 * 退票是否成功
	 */
	private Boolean returnSuccess;
	/**
	 * 退款金额(成功有值)
	 */
	private String returnMoney;
	/**
	 * 保险退款金额
	 */
	private String insureReturnMoney;
	/**
	 * 退票成功的时间
	 */
	private String returnTime;
	/**
	 * 失败原因编号
	 */
	private String returnFailId;
	/**
	 * 失败原因信息，与失败原因编号对应
	 */
	private String returnFailMsg;

	public TuniuRefundTicket() {
		super();
	}

	public TuniuRefundTicket(String ticketNo, String passengerName,
			String passportTypeId, String passportNo) {
		super();
		this.ticketNo = ticketNo;
		this.passengerName = passengerName;
		this.passportTypeId = passportTypeId;
		this.passportNo = passportNo;
	}

	public String getTicketNo() {
		return ticketNo;
	}

	public void setTicketNo(String ticketNo) {
		this.ticketNo = ticketNo;
	}

	public String getPassengerName() {
		return passengerName;
	}

	public void setPassengerName(String passengerName) {
		this.passengerName = passengerName;
	}

	public String getPassportTypeId() {
		return passportTypeId;
	}

	public void setPassportTypeId(String passportTypeId) {
		this.passportTypeId = passportTypeId;
	}

	public String getPassportNo() {
		return passportNo;
	}

	public void setPassportNo(String passportNo) {
		this.passportNo = passportNo;
	}

	public Boolean getReturnSuccess() {
		return returnSuccess;
	}

	public void setReturnSuccess(Boolean returnSuccess) {
		this.returnSuccess = returnSuccess;
	}

	public String getReturnMoney() {
		return returnMoney;
	}

	public void setReturnMoney(String returnMoney) {
		this.returnMoney = returnMoney;
	}

	public String getInsureReturnMoney() {
		return insureReturnMoney;
	}

	public void setInsureReturnMoney(String insureReturnMoney) {
		this.insureReturnMoney = insureReturnMoney;
	}

	public String getReturnTime() {
		return returnTime;
	}

	public void setReturnTime(String returnTime) {
		this.returnTime = returnTime;
	}

	public String getReturnFailId() {
		return returnFailId;
	}

	public void setReturnFailId(String returnFailId) {
		this.returnFailId = returnFailId;
	}

	public String getReturnFailMsg() {
		return returnFailMsg;
	}

	public void setReturnFailMsg(String returnFailMsg) {
		this.returnFailMsg = returnFailMsg;
	}

	@Override
	public String toString() {
		return "TuniuRefundTicket [insureReturnMoney=" + insureReturnMoney
				+ ", passengerName=" + passengerName + ", passportNo="
				+ passportNo + ", passportTypeId=" + passportTypeId
				+ ", returnFailId=" + returnFailId + ", returnFailMsg="
				+ returnFailMsg + ", returnMoney=" + returnMoney
				+ ", returnSuccess=" + returnSuccess + ", returnTime="
				+ returnTime + ", ticketNo=" + ticketNo + "]";
	}

}
