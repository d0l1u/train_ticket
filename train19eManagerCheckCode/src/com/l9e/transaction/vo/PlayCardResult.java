package com.l9e.transaction.vo;

public class PlayCardResult {
	private String total_num;
	
	private String right_num;
	
	private String error_num;
	
	private String over_num;

	public String getTotal_num() {
		return total_num;
	}

	public void setTotal_num(String totalNum) {
		total_num = totalNum;
	}

	public String getRight_num() {
		return right_num;
	}

	public void setRight_num(String rightNum) {
		right_num = rightNum;
	}

	public String getError_num() {
		return error_num;
	}

	public void setError_num(String errorNum) {
		error_num = errorNum;
	}

	public String getOver_num() {
		return over_num;
	}

	public void setOver_num(String overNum) {
		over_num = overNum;
	}
	
}
