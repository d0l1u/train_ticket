package com.l9e.transaction.vo;

/**
 * @author Administrator
 *  携程  ，站名
 */
public class CtripStation {
	
	
	private String name;//站名
	private String pinyin;//拼音
	private String cc_status;//初始状态
	
	public CtripStation() {
		super();
	}

	public CtripStation(String name, String pinyin) {
		super();
		this.name = name;
		this.pinyin = pinyin;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}

	
	public String getCc_status() {
		return cc_status;
	}

	public void setCc_status(String cc_status) {
		this.cc_status = cc_status;
	}
	
	

}
